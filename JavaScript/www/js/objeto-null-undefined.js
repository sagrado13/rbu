"use stricit";

/* STRING */
let string1 = "texto";
console.log(typeof string1);

/* NUMBER */
let number1 = 1;
console.log(typeof number1);

/* OBJECT */
let objeto1 = {
  propiedad1: "texto1",
  prop2: 1,
  prop3: {},
};

console.log(objeto1);
console.log(typeof objeto1);

console.log(objeto1.propiedad1);
console.log(typeof objeto1.propiedad1);

console.log(objeto1.prop2);
console.log(typeof objeto1.prop2);

console.log(objeto1.prop3);
console.log(typeof objeto1.prop3);

/* NULL */
console.log(null);
console.log(typeof null);

/* UNDEFINED */
console.log(undefined);
console.log(typeof undefined);

/* BOOLEAN */
let uno = true;
let dos = false;
console.log(typeof uno, typeof dos);
